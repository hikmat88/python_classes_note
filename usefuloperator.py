# min and max

my_list=[10,5,20,26,50,35,40]
print(max(my_list))
print(min(my_list))

#shuffel means print the value in random order
from random import shuffle
shuffle(my_list)
print(my_list)

#for random integer use randint
from random import randint
print(randint(0,50))

